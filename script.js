$(document).ready(function(){
  $('#greenTea').on("click", function() {
      $('#tea').css({ fill: "#a9cc97" });
  });
  $('#redTea').on("click", function() {
      $('#tea').css({ fill: "#cc4807" });
  });
  $('#blackTea').on("click", function() {
      $('#tea').css({ fill: "#ac8f7d" });
  });


});
